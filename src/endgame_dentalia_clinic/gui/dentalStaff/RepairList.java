    public ArrayList<Repair> repairList(){
        ArrayList<Repair> repairList= new ArrayList<>();
        
        try{
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/endgamedentaliaclinic2","root","");
            String sql="SELECT * FROM repairmachine, staff WHERE repairmachine.user_id=staff.user_id AND staff.username='"+username+"'";
            st = con.createStatement();
            rs= st.executeQuery(sql);
            Repair repair;
            System.out.print(sql);
            while(rs.next()){
                repair = new Repair(rs.getInt("id"), rs.getInt("user_id"), rs.getString("machine_no"), rs.getString("room_no"), rs.getString("problem"), rs.getString("date"));
                repairList.add(repair);
            }
        }catch(SQLException | HeadlessException  ex){
                JOptionPane.showMessageDialog(null,ex.getMessage(),"Oh Snap!", JOptionPane.WARNING_MESSAGE);
        }
        
        return repairList;
    }
    public void show_repair(){
        ArrayList<Repair> list =  repairList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        Object[] row = new Object[5];
        for(int i=0; i<list.size();i++){
            row[0] = list.get(i).getMachine_no();
            row[1] = list.get(i).getRoom_no();
            row[2] = list.get(i).getDate();
            row[3] = list.get(i).getProblem();
            model.addRow(row);
        }
    }
    
