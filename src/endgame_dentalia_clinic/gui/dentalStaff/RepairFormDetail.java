    public void repairForm_detail(){
        try{
            
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/endgamedentaliaclinic2","root","");
            String sql="SELECT * FROM  repairmachine WHERE machine_no=? ";
            
            pst=con.prepareStatement(sql);
            pst.setString(1, machine_no);
            rs=pst.executeQuery();
            
            if(rs.next()){
                machineid = rs.getString("id"); 
                roomno = rs.getString("room_no");
                problem =rs.getString("problem");
                date =rs.getString("date");

            }
            
        }catch(SQLException | HeadlessException  ex){
                JOptionPane.showMessageDialog(null,ex.getMessage(),"Oh Snap!", JOptionPane.WARNING_MESSAGE);
        }
        }
    
